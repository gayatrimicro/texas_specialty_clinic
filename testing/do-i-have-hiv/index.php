      <p>The symptoms of an early HIV infection are similar to severe influenza (flu) or infectious mononucleosis, otherwise known as glandular fever. From a few days up to a few months later, the symptoms disappear on their own, but the HIV infection is still there.</p>

      <p>Symptoms of an early HIV infection do not appear as soon as you have the virus, but 10 days to a few months later. So if you've had unprotected sex* and you feel ill the next day, this is not an HIV infection. Keep in mind, if you start noticing symptoms 10 days or longer after unprotected sex; this may indicate an early HIV infection.</p> 

      <p>If you’ve had unprotected sex but show no symptoms, you should get tested for HIV around 6 weeks later. You don’t always notice an HIV infection.</p>

