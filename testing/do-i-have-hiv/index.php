    <h4 style="margin-top: 0px;">Living with HIV</h4>
      <p>Luckily, these days the impact of HIV on your health is minimal as long as you get treated. Treatment is in the form of taking medication and having regular check-ups at the hospital. Usually you only have to take 1 pill a day. People with HIV have the same life expectancy as people who don’t have HIV. The sooner you start your treatment, the better it is for your health.

      <h6 style="margin-top: 0px;margin-bottom: 0px;">U=U</h6>
      <p style="margin-top: 5px;">HIV treatment ensures that the virus is supressed. At a certain point, the virus is supressed to such an extent, that it is undetectable in your blood. You then have an undetectable viral load. People with an undetectable viral load cannot transmit the virus to other people (U=U, undetectable = untransmittable). So you can have sex with other people without passing the virus on to them.</p>

      <h6 style="margin-top: 0px;">You’re not alone</h6>
      <p style="margin-top: 5px;">Despite these positive developments, many people are shocked by their HIV diagnosis. Luckily, you’re not on your own. You can talk to your doctor, HIV consultant, or contact Texas Speciality Clinic. They organize all kinds of activities for people living with HIV and provide information about living with HIV. It can help to see how other people live with HIV as part of everyday life. Of course, you can also go to a colleague or friend for support. </p>
