      <div class="container container--xsmall" style="margin-top: 0px;">
        <h4 style="margin-top: 0px;">What is gender non-conformity/diversity/incongruence?</h4>
          <p>Gender non-conformity is when the gender identity and/or gender expression differs from what is typically associated with the sex assigned at birth. Gender non-conforming behaviors are common in childhood and do not necessarily lead to gender dysphoria.</p>

          <br><br>

          <h4 style="margin-top: 0px;">What is gender dysphoria?</h4>
          <p>Gender dysphoria is the distress and unease that develops when gender identity is not the same as the designated sex at birth. Gender dysphoria manifests differently in different age groups. Children may insist that they are a different gender from that assigned at birth, may prefer roles and play of a different gender, and may develop body dysphoria (discomfort about their body parts).<br><br> Body dysphoria often becomes more common as children with gender dysphoria approach adolescence and may become severe during puberty, as bodies mature. If distress is worsened by changes with puberty, persistence into adulthood is likely.</p>

          <br><br>

          <h4 style="margin-top: 0px;">When should I contact my health care provider?</h4>
          <p>If gender nonconformity is of concern to the patient or caregiver(s), your health care provider may refer you to a mental health provider who has experience working with transgender or gender diverse children and adolescents.</p>

          <br><br>

          <h4 style="margin-top: 0px;">When can hormone therapy for gender dysphoria be initiated?</h4>
          <p>Hormone therapy before puberty is not needed because testosterone or estrogen would not be present in children before puberty. Puberty blocking medication may be started once the child shows physical signs of puberty (growth of testes or breast development).</p>

          <br><br>

          <h4 style="margin-top: 0px;">What are puberty blockers?</h4>
          <p>Children with gender dysphoria may wish to avoid body changes associated with puberty. In this setting, puberty blocking medications could be considered once an experienced clinician confirms the start of puberty by physical exam and gender dysphoria has been confirmed by a qualified mental health provider.<br><br>

          Puberty-blocking medications are given by injections every 1-3 months or by under-skin implants lasting 1 year, which stop testosterone or estrogen from being made. By doing this, physical changes associated with puberty will not occur.<br><br>

          Puberty blockers allow more time to explore gender identity, live in the experienced gender, and understand the medical and/or surgical options. They also avoid unwanted sexual development and, in later pubertal stages, stop periods and prevent further facial hair growth/voice deepening. Puberty-blocking medications are fully reversible.<br><br>

          Risks of puberty blockers include infertility, particularly if the blockers are started in early puberty. This should be discussed with your medical provider prior to starting puberty blockers. Puberty blockers may adversely affect bone development, and bone density may be evaluated during treatment. Other side effects may include headaches, hot flashes, fatigue, and mood alterations. For transgender girls, puberty blocking medications will limit tissue available from penile and scrotal growth for future surgical treatments if started in early puberty.</p>

          <br><br>

          <h4 style="margin-top: 0px;">Other medications to stop puberty</h4>
          <p>Some youth with gender dysphoria may want to block their hormones using medications other than puberty-blocking medications.<br><br>

              Transgender boys may want to suppress menstrual periods. In this case, progestin-only birth control medications by injection, intrauterine device (IUD), under-skin implant, or pills may sufficiently suppress periods. Risks of these medications include mood changes, weight gain, and low bone mineral density.<br><br>

              Transgender girls may want to block testosterone effects, which can be accomplished by an oral medication called spironolactone. Risks include high potassium levels, increased urination, and decreased blood pressure.</p>

          <br><br>

          <h4 style="margin-top: 0px;">Gender-Affirming Hormone Therapy for Teens</h4>
          <p>Teens may wish to be treated with hormones (estrogen or testosterone) that affirm their gender identity. These teens should see a qualified mental health professional who can confirm gender dysphoria and treat any psychological problems that might interfere with the safety of hormone therapy.<br><br> This mental health professional also provides helpful support to the teen emotionally as they undergo physical changes related to the hormone therapy. A medical provider will discuss risks and benefits of the hormone therapies with the teen and their family before prescribing.<br><br>

          Age of starting hormone therapy will be determined based on discussion with the patient, family, and health care team. Medical providers will talk with patients and families about options for fertility preservation in adolescents, prior to starting treatment.<br><br> Hormone medications will be given using a gradually increasing dose schedule to mimic the new puberty of the identified gender. Desired effects of gender-affirming hormone therapy take months to years to become fully complete and natural variations in response can be expected (just as with typical puberty).
          </p>
          <h5 style="margin-top: 0px;margin-bottom: 0px;">Estrogen therapy</h5>
          <p style="margin-top: 0px;margin-bottom: 0px;">Estrogen therapy for transgender girls is provided most commonly by pills or patches. Other options include gels and injections. Feminizing changes include softening of skin, decreased muscle bulk, redistribution of fat, breast growth, and decreased body hair growth. Breast growth is irreversible once developed. No voice changes (higher pitch) occur with estrogen therapy. Estrogen is generally safe in young healthy teens or adults, however, risks should be discussed with your health care provider. </p>
          <br><br>

          <h5 style="margin-top: 0px;margin-bottom: 0px;">Testosterone therapy</h5>
          <p style="margin-top: 0px;margin-bottom: 0px;">Testosterone therapy for transgender boys includes injection into muscle or skin every 1-2 weeks, gels, and patches. Effects include acne, facial/body hair, scalp hair loss, increased muscle bulk, menstrual periods stop (can take a few months to years), clitoral enlargement, vaginal dryness, and voice deepening. Clitoral growth and voice deepening are both irreversible effects. Testosterone is generally safe in young healthy teens or adults; however, risks should be discussed with your health care provider.<br><br>

          Youth taking gender-affirming therapy should be seen by their medical provider every 3-6 months until adult dosing is established, then every 6-12 months thereafter to assess for desired and adverse effects and to measure hormone levels. </p>

          <br><br>

          <h4 style="margin-top: 0px;">Gender-Affirming Surgery for Teens</h4>
          <p>In the United States, adolescents are not able to consent for removal of their ovaries/testes until they are 18 years old. However, some surgeons may perform surgical removal of breast tissue (“top surgery”) in adolescents younger than 18 years who are able to fully understand the risks and benefits. In this setting, a qualified mental health professional must confirm gender dysphoria, confirm that any psychological or social problems have been addressed and are stable, and confirm that the adolescent is emotionally mature enough to consent to the procedure.</p>

